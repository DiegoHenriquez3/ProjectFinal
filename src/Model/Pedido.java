package Model;

import java.util.Date;
import java.util.List;

/**
 *
 * @author Diego
 */
public class Pedido {
   
    private int idPedido;
    private int estado;
    private String fechaPedido;
    private Bodega idBodega;
    private Empleado idEmpleado;
    private Sucursal idSucursal;
    private int cantidad;
    private double costo;
    private List<Zapato> zapatosList;
    
    

    public int getIdPedido() {
        return idPedido;
    }

    public void setIdPedido(int idPedido) {
        this.idPedido = idPedido;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }

    public String getFechaPedido() {
        return fechaPedido;
    }

    public void setFechaPedido(String fechaPedido) {
        this.fechaPedido = fechaPedido;
    }

    public Bodega getIdBodega() {
        return idBodega;
    }

    public void setIdBodega(Bodega idBodega) {
        this.idBodega = idBodega;
    }

    public Empleado getIdEmpleado() {
        return idEmpleado;
    }

    public void setIdEmpleado(Empleado idEmpleado) {
        this.idEmpleado = idEmpleado;
    }

    public Sucursal getIdSucursal() {
        return idSucursal;
    }

    public void setIdSucursal(Sucursal idSucursal) {
        this.idSucursal = idSucursal;
    }

    public List<Zapato> getZapatosList() {
        return zapatosList;
    }

    public void setZapatosList(List<Zapato> zapatosList) {
        this.zapatosList = zapatosList;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public double getCosto() {
        return costo;
    }

    public void setCosto(double costo) {
        this.costo = costo;
    }
    
    
    
    
    
}
